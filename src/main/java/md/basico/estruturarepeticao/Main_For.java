package md.basico.estruturarepeticao;

public class Main_For {

	public static void main(String[] args) {
		
		//Incremento
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
		//Decremento
		for (int i = 10; i >0; i--) {
			System.out.print(i + " ");
		}
		
		
	}

}
